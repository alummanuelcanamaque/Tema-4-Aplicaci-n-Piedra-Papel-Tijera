    private void boton_JugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_JugarActionPerformed
        label_Resultado.setText(Juego.compararJugadas());        
        label_Recuento.setText(Juego.getPartidasGanadas()+" a "+Juego.getPartidasPerdidas());
        label_Manos.setText(Juego.getMovimientoJugadorToString()+" vs "+ Juego.getMovimientoMaquinaToString());
        boton_Jugar.setEnabled(false);
        
        //Si se ha acabado el juego, mostramos un JOptionPane con las posibles opciones.
        if(Juego.finJuego()){
            JOptionPane.showMessageDialog(this, "Ganador: "+Juego.ganadorToString());
            int seleccion = JOptionPane.showOptionDialog( this,"¬øQu√© desea hacer a continuaci√≥n?",
            "Fin del juego",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE,
            null, new Object[] { "Jugar de nuevo", "Cambiar opciones", "Salir" },"Jugar de nuevo");
            switch(seleccion){
                case 0:
                    Juego.reiniciarJuego();
                    label_Resultado.setText("");        
                    label_Recuento.setText(Juego.getPartidasGanadas()+" a "+Juego.getPartidasPerdidas());
                    label_Manos.setText("");
                    boton_Jugar.setEnabled(false);
                    break;
                case 1:
                    Juego.reiniciarJuego();
                    this.setVisible(false);
                    MenuInicio menu = new MenuInicio();
                    menu.setVisible(true);
                    break;                    
                case 2:
                default:
                    System.exit(0);
                    break;
            }  
        }        
    }//GEN-LAST:event_boton_JugarActionPerformed

